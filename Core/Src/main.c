void print_text(char * text)
{
	int buf_size = 0;
	char buff[50] = {0};

	for(buf_size = 0; buf_size <= 100; buf_size++)
	{
		if(text[buf_size] == '\r')
		{
			buf_size++;
			break;
		}
	}

	memset(buff, text, buf_size);
	HAL_UART_Transmit_IT(&huart3, text, buf_size);
	HAL_Delay(10);
}
